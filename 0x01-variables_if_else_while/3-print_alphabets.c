#include <stdio.h>
/**
 * main - entry point
 *
 * Description: putchar to print lower and upper alphabet
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	char(x);
	char(N);

	for (x = 'a'; x <= 'z'; x++)
	{
		putchar (x);
	}
	for (N = 'A'; N <= 'Z'; N++)
	{
		putchar (N);
	}
	putchar ('\n');

	return (0);
}
